// Returns an approximation of logₑ x.
// The algorithm was first published in 1994 by Jean-Claude Bajard, Sylvanus
// Kla, and Jean-Michel Muller. It relies on the fact that every real number
// r where 1 < r < 2 can be represented as a product of distinct factors of
// the form 1+2⁻ⁿ. Given these factors fₙ so that r = ∏fₙ we have logₑ(r) =
// ∑logₑ(fₙ).
pub fn bkm_l(x: &FP492) -> FP492 {
    if x == &FP492::ONE {
        // ln(1) = 0
        return FP492::ZERO;
    }
    // 1 < x < 3
    let mut x: U512 = x.into();
    let sh = FP492::INT_BITS - 2;
    debug_assert!(x.leading_zeros() >= sh);
    x <<= sh;
    let mut p = U512::new(1_u128 << 125, 0_u128, 0_u128, 0_u128);
    debug_assert!(p <= x, "{p:?}\n{x:?}");
    let mut ln = U512::ZERO;
    for (n, l) in LUT.iter().enumerate() {
        // t = p⋅(1+2⁻ⁿ) = p + p⋅2⁻ⁿ
        let t = p + (p >> n as u32);
        match &t.cmp(&x) {
            Ordering::Less => {
                ln += l;
                p = t;
            }
            Ordering::Equal => {
                ln += l;
                break;
            }
            _ => {}
        }
    }
    FP492::from(ln.rounding_div_pow2(FP492::INT_BITS))
}
